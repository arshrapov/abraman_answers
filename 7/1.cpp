#include <iostream>

int main () {
	using namespace std;
	int n; cin >> n;
	switch (n) {
		case 1:
			cout << "Number is One";
			break;
		case 2:
			cout << "Number is Two";
			break;
		case 3:
			cout << "Number is Seven";
			break;
		case 4:
			cout << "Number is Four";
			break;
		case 5:
			cout << "Number is Five";
			break;
		case 6:
			cout << "Number is Six";
			break;
		case 7:
			cout << "Number is Seven";
			break;
		default:
			cout << "Unknown number";
	}
	cout << endl;
	return 0;
}
